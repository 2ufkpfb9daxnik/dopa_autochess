def build_distinctive_marks(count: int = 121) -> list[str]:
    marks: list[str] = []
    seen: set[str] = set()
    n_v, n_h, n_c = len(VISUAL_TRAITS), len(HABIT_BITS), len(MICRO_BACKSTORY)
    i = 0
    while len(marks) < count:
        v = VISUAL_TRAITS[i % n_v]
        h = HABIT_BITS[(i * 7 + 3) % n_h]
        c = MICRO_BACKSTORY[(i * 11 + 5) % n_c]
        mark = f"{v}。{h}{c}"
        if mark not in seen:
            seen.add(mark)
            marks.append(mark)
        i += 1
        if i > count * 20:
            marks.append(f"{v}（{len(marks)}）。")
            seen.add(mark)
    return marks
